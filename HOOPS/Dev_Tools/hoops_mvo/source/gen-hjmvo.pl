#!/usr/bin/perl -w --

use strict;
use Getopt::Std;
 
getopts("i:");

my $swig_ifile;
if ($Getopt::Std::opt_i)
{
	$swig_ifile ="$Getopt::Std::opt_i";
} else {
	$swig_ifile ="hjmvo.i";
}

# run swig
chdir ("java_mvo");
unlink(<*.java>); # Remove existing .java files.  All .java files should be generated by swig. 
my $foo = $swig_ifile =~ /64/ ? '64' : '';
system "swig-2.0.4 -v -java -c++ -package com.techsoft.hoops$foo -o hjmvo_wrap.cxx $swig_ifile";
exit;

